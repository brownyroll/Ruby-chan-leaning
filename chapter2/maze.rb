$cache = {}
def maze(x,y)
  return 0 if x < 0 
  return 0 if y < 0 
  return 0 if y >= 50 
  return 0 if x >= 50 
  if [x,y] == [0,0]
    return $maze[0][0]
  end
  # thx p'thai dt.in.th 
  $cache[[x,y]] ||= begin
    score_from_left = maze(x - 1 ,y)
    score_from_above = maze(x,y -1)
    score_this = $maze[y][x]
    [score_this,score_from_above].max + score_this
    rescue 
  end
end

